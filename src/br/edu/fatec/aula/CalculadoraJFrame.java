public void calcular(){
    
    
    if (num2 == 0 && operacao == '/'){
        JOptionPane.showMessageDialog(null,"Operação invalida!"); 
        reiniciarcalculadora();
    }else {
     if (operacao == '+'){
        resultado = num1+num2;
    }else if (operacao == '/'){
        resultado = num1/num2;
    }else if (operacao == '*'){
        resultado = num1*num2;
    }else if (operacao == '-'){
        resultado = num1-num2;
    }else {
        resultado = Math.sqrt(num1);
    }
     
    resultadoTextField.setText(String.valueOf(resultado));
    }
    jPanel1.requestFocus();
    
    
    
}

