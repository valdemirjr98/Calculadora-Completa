public void setarNumerosNoResultadoTxV (char numero){
    
    if("0".equals(resultadoTextField.getText()) && !".".equals(String.valueOf(numero))){
        resultadoTextField.setText("");   
    }
    
    String texto = resultadoTextField.getText();
    
    if(texto.length() <= 14){
    texto = texto+numero;
    resultadoTextField.setText(texto);
    
    jPanel1.requestFocus();
    }
}

